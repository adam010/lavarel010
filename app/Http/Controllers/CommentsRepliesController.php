    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $replies=Comment::findOrFail($id)->replies()->get();

        
        return view('admin.comments.replies.index',compact('replies'));
        
    }

